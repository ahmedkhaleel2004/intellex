export const userSignOut = () => {
	return new Promise<void>((resolve, reject) => {
		signOut(auth)
			.then(() => {
				// Sign-out successful.
				resolve();
			})
			.catch((error) => {
				// An error happened.
				reject(error);
			});
	});
};